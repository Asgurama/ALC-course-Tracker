function renderALC() {
    const container = document.getElementById('alc-container');
    for (const state in alcData) {
        const stateHeader = document.createElement('h2');
        stateHeader.textContent = state;
        container.appendChild(stateHeader);

        const list = document.createElement('ul');
        alcData[state].forEach(hub => {
            const listItem = document.createElement('li');
            listItem.textContent = hub;
            list.appendChild(listItem);
        });
        container.appendChild(list);
    }
}

document.addEventListener('DOMContentLoaded', renderALC);
